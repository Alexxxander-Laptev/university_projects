NAME vivod

PUBLIC	vivod 
vivod_ROUTINES SEGMENT CODE
RSEG vivod_ROUTINES
JMP vivod
vivod:
	JNB  TI,$		; Ждем, пока появится флаг прерывания передатчика
	CLR  TI			; Очищаем флаг прерывания передатчика
	MOV  SBUF,A		; Отправляем байт в буфер передачи
	mov A, #0 
RET
END
































































































































